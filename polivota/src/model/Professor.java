package model;

public class Professor {

	private int nusp;
	private String email;
	private String link_lattes;
	private String nome;
	private String sala;
	private String path_foto;
	
	public int getNusp() {
		return nusp;
	}
	public void setNusp(int nusp) {
		this.nusp = nusp;
	}
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}
	public String getLink_lattes() {
		return link_lattes;
	}
	public void setLink_lattes(String link_lattes) {
		this.link_lattes = link_lattes;
	}
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	public String getSala() {
		return sala;
	}
	public void setSala(String sala) {
		this.sala = sala;
	}
	public String getPath_foto() {
		return path_foto;
	}
	public void setPath_foto(String path_foto) {
		this.path_foto = path_foto;
	}
	
	
	
}
